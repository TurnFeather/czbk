package cn.edu.ncist.dataAnalyze.realtime.vo;

public class AllYearAcciNumModel {
	
	
	String year;
	String die_num;
	String acd_num;
	
	
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getDie_num() {
		return die_num;
	}
	public void setDie_num(String dieNum) {
		die_num = dieNum;
	}
	public String getAcd_num() {
		return acd_num;
	}
	public void setAcd_num(String acdNum) {
		acd_num = acdNum;
	}
	
	
	
	
	

}
